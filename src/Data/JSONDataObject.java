package Data;

public class JSONDataObject {
    private String overNode;
    private String underNode;
    private String underNodeData;


    public String getOverNode() {
        return overNode;
    }

    public void setOverNode(String overNode) {
        this.overNode = overNode;
    }

    public String getUnderNode() {
        return underNode;
    }

    public void setUnderNode(String underNode) {
        this.underNode = underNode;
    }

    public String getUnderNodeData() {
        return underNodeData;
    }

    public void setUnderNodeData(String underNodeData) {
        this.underNodeData = underNodeData;
    }
}
